<?php
// Dekralasi variabel
$celcius = 37.841;

// Konversi suhu
$fahrenheit = (9/5) * $celcius + 32;
$reamur = (4/5) * $celcius;
$kelvin = $celcius + 273.15;

// Menampilkan hasil konversi suhu
echo "Fahrenreit (F) : $fahrenheit<br>";
echo "Reamur (R) : $reamur<br>";
echo "Kelvin (K) : $kelvin<br>";
?>